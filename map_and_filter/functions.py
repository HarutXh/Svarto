def pos_num_in_str(num):
    return num in "0123456789"


def is_even(n):
    return n % 2 == 0


def is_odd(n):
    return n % 2 == 1


def vowels(char):
    return char in "aeiouAEIOU"


def is_negative(n):
    return n < 0


def add_2000(x):
    return x + 2000


def is_below_8000(x):
    return x < 8000
